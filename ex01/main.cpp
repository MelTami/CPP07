#include "iter.hpp"

int main(void)
{
    int int_array[] = {1, 2, 3, 4, 5};
    std::string str_array[] = {"one", "two", "three", "four", "five"};
    float float_array[] = {1.1f, 2.2f, 3.3f, 4.4f, 5.5f};
    double double_array[] = {1.11, 2.22, 3.33, 4.44, 5.55};
    char char_array[] = {'a', 'b', 'c', 'd', 'e'};

    std::cout << std::endl;
    std::cout << "----------------------- ITER TEST -----------------------------" << std::endl;
    std::cout << "---------------------------------------------------------------" << std::endl;
    std::cout << "----------------------- INT TEST ------------------------------" << std::endl;
    std::cout << "---------------------------------------------------------------" << std::endl;
    ::iter<int>(int_array, 5, ::printChar<int>);
    std::cout << "---------------------------------------------------------------" << std::endl;
    std::cout << "----------------------- STRING TEST ---------------------------" << std::endl;
    std::cout << "---------------------------------------------------------------" << std::endl;
    ::iter(str_array, 5, ::printChar<std::string>);
    std::cout << "---------------------------------------------------------------" << std::endl;
    std::cout << "----------------------- FLOAT TEST ----------------------------" << std::endl;
    std::cout << "---------------------------------------------------------------" << std::endl;
    ::iter(float_array, 5, ::printChar<float>);
    std::cout << "---------------------------------------------------------------" << std::endl;
    std::cout << "----------------------- DOUBLE TEST ---------------------------" << std::endl;
    std::cout << "---------------------------------------------------------------" << std::endl;
    ::iter(double_array, 5, ::printChar<double>);
    std::cout << "---------------------------------------------------------------" << std::endl;
    std::cout << "----------------------- CHAR TEST -----------------------------" << std::endl;
    std::cout << "---------------------------------------------------------------" << std::endl;
    ::iter(char_array, 5, ::printChar<char>);
    std::cout << "---------------------------------------------------------------" << std::endl;
    std::cout << "----------------------- END TEST ------------------------------" << std::endl;

    return (0);
}